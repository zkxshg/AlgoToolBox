// Determine if char is a Vowel

bool ifVowel(char c) {
    if (c == 'a' || c == 'A') return true;
    if (c == 'e' || c == 'E') return true;
    if (c == 'i' || c == 'I') return true;
    if (c == 'o' || c == 'O') return true;
    if (c == 'u' || c == 'U') return true;
    return false;
}
